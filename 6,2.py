import math as m
def tri(x1,y1,x2,y2,x3,y3,x4,y4,x5,y5):
    s1=m.fabs((x2-x1)*(y3-y1)-(y2-y1)*(x3-x1))/2
    s2=m.fabs((x3-x2)*(y4-y2)-(y3-y2)*(x4-x2))/2
    s3=m.fabs((x4-x3)*(y5-y3)-(y4-y3)*(x5-x3))/2
    print("Сумма площади треугольника 1=",s1)
    print("Сумма площади треугольника 2=",s2)
    print("Сумма площади треугольника 3=",s3)
    s=s1+s2+s3
    print("Сумма площади пятиугольника=",s)
tri(3,5,1,15,16,11,21,9,10,32)
